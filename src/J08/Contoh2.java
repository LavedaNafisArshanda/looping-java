package J08;

public class Contoh2 {
     public static void main(String[] args)
    {
        //PERULANGAN FOR
        int x;
        for(x = 1; x <= 10; x++)
        {
            System.out.print(x + " ");
        }
        
         // PERULANGAN WHILE
       
        while(x <= 10)
        {
            System.out.println(x + " ");
            x++;
        }
        
        //PERULANGAN DO WHILE
        do
        {
            System.out.println(x + " ");
            x++;
        }
        while(x <= 10);
        
        }
    }

        
        

